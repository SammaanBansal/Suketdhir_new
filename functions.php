/**
* Replacing the sub-menu class from menus.
*/

class my_nav_walker extends Walker_Nav_Menu {
  function start_lvl(&$output, $depth) {
    $indent = str_repeat("\t", $depth);
    $output .= "\n$indent<ul class=\"header-submenu\">\n";
  }
}

